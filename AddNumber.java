class AddNumbers
{
    public static void main(String[] args)
    {
        int a = 20;
        int b = 10;

        int sum = a + b;

        System.out.println("First Number  : " + a);
        System.out.println("Second Number : " + b);
        System.out.println("Sum = " + sum);
    }
}
